#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char		*d;
	const unsigned char	*s;

	d = (unsigned char *)dst;
	s = (const unsigned char *)src;
	if (d > s)
	{
		while (len-- != 0)
			d[len] = s[len];
	}
	else
		ft_memcpy(d, s, len);
	return (d);
}
